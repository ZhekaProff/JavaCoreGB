package ru.zheka.seminar01.methods;

public class Operator {

    public static int addition(int firstCount, int secondCount){

        return firstCount + secondCount;
    }

    public static int subtraction(int firstCount, int secondCount){

        return firstCount - secondCount;
    }

    public static int multiplication(int firstCount, int secondCount){

        return firstCount * secondCount;
    }

    public static float division(int firstCount, int secondCount){

        return (float) (firstCount / secondCount);
    }

}
